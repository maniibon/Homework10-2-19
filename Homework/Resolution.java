package Homework;

public class Resolution {
    int width;
    int height;
 public Resolution(int width, int height) {
 this.width = width;
 this.height = height;
}

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
